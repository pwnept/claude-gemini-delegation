def main():
    """Enhanced installation flow."""
    # Import from basic installer
    from install import (
        check_python_version,
        discover_clis,
        interactive_selection,
        setup_hooks,
        generate_delegation_config,
        save_config,
        extract_migrated_claude_content,
        ensure_agents_md,
        ensure_root_claude_bridge,
        ensure_dot_claude_bridge,
        install_not_found_clis
    )

    parser = argparse.ArgumentParser(
        description="Install Claude Code/Codex delegation hooks for Antigravity CLI."
    )
    parser.add_argument(
        "--enable-cli",
        action="append",
        choices=("agy", "aider", "copilot", "gpt-me"),
        dest="enabled_clis",
        help="Enable an additional delegation CLI. Repeat for multiple CLIs. Default: agy only.",
    )
    parser.add_argument(
        "--all-clis",
        action="store_true",
        help="Enable every supported CLI that is installed.",
    )
    parser.add_argument(
        "--target",
        type=str,
        default=None,
        help="Target directory for installation (default: current working directory)",
    )
    parser.add_argument(
        "--update-all",
        action="store_true",
        help="Update all globally registered installations with the latest hook scripts.",
    )
    parser.add_argument(
        "--stage",
        action="store_true",
        help="Stage installer-generated files when the target is a Git repository.",
    )
    args = parser.parse_args()
    
    if args.update_all:
        if not HOOKS_SOURCE.exists():
            print_error(f"Hooks source directory not found: {HOOKS_SOURCE}")
            sys.exit(1)
        update_all_installations(HOOKS_SOURCE)
        sys.exit(0)
    
    print_header("Claude/Codex -> Antigravity Delegation Setup")
    
    # Check Python version
    if not check_python_version():
        sys.exit(1)
    
    # Determine installation location
    if args.target:
        base_dir = Path(args.target) / ".claude"
    else:
        base_dir = Path.cwd() / ".claude"
    print_info(f"Installing to: {base_dir.absolute()}")
    
    # Discover CLIs
    discovered = discover_clis()
    
    # CLI selection. Default is agy only; other CLIs require explicit flags.
    enabled_cli_names = ["agy"] + (args.enabled_clis or [])
    configured = interactive_selection(
        discovered,
        enabled_cli_names=enabled_cli_names,
        enable_all=args.all_clis,
    )
    
    # Setup hooks directory
    setup_hooks(base_dir)
    
    # Copy hook files
    # Generate config first — needed by install_gemini_delegation_dir
    config = generate_delegation_config(configured)
    presets = build_routing_presets(config)

    # Install shared scripts to .gemini-delegation/ (single copy for both envs)
    if HOOKS_SOURCE.exists():
        install_gemini_delegation_dir(base_dir.parent, config)
    else:
        print_warning(f"Hooks source directory not found: {HOOKS_SOURCE}")

    # Install per-env shims (thin forwarders to .gemini-delegation/hooks/)
    hooks_dir = base_dir / "hooks"
    setup_hooks(base_dir)
    create_env_shims(hooks_dir)
    create_claude_settings(base_dir)

    codex_dir = get_codex_dir(base_dir.parent)
    setup_hooks(codex_dir)
    create_env_shims(codex_dir / "hooks")

    # Migrate any legacy .claude/CLAUDE.md content into AGENTS.md.
    dot_claude_migrated = ensure_dot_claude_bridge(base_dir)
    root_claude_md = base_dir.parent / "CLAUDE.md"
    root_claude_existing = root_claude_md.read_text(encoding="utf-8") if root_claude_md.exists() else ""
    root_migrated = extract_migrated_claude_content(root_claude_existing)
    combined_migration = (
        root_migrated.rstrip("\n") + "\n\n" + dot_claude_migrated
        if dot_claude_migrated.strip() else root_migrated
    )
    ensure_agents_md(
        base_dir.parent,
        combined_migration,
        managed_body=_build_claude_md_body(config, presets),
    )
    ensure_root_claude_bridge(base_dir.parent)
    create_antigravity_rule(base_dir.parent)

    create_usage_examples(config, base_dir)
    create_usage_examples(config, codex_dir)

    # Install bundled agents (Dave, etc.)
    install_agents(base_dir.parent)

    # Create temp/ for delegation response files
    ensure_temp_dir(base_dir.parent)

    # Verify installation
    if not verify_installation(base_dir, config):
        print_error("Installation verification failed")
        sys.exit(1)

    if args.stage:
        stage_generated_files(base_dir.parent, base_dir, codex_dir)

    # Show next steps
    show_next_steps(config)

    # Offer to install missing CLIs
    install_not_found_clis(configured)

    print_header("Installation Complete!")
    print(f"\n{Colors.GREEN}{Colors.BOLD}Your delegation setup is ready!{Colors.END}\n")


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print(f"\n\n{Colors.YELLOW}Installation cancelled.{Colors.END}")
        sys.exit(0)
    except Exception as e:
        print_error(f"Installation failed: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)
